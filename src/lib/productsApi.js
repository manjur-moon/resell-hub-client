import axiosInstance from "./axiosInstance";
export const getProductsApi=async(params={})=>(await axiosInstance.get('/api/products',{params})).data;
export const getFeaturedProductsApi=async(params={})=>(await axiosInstance.get('/api/products/featured',{params})).data;
export const getProductCategoriesApi=async()=>(await axiosInstance.get('/api/products/categories')).data;
export const createProductApi=async(payload)=>(await axiosInstance.post('/api/products',payload)).data;
export const getMyProductsApi=async(params={})=>(await axiosInstance.get('/api/products/seller/my-products',{params})).data;
export const getProductByIdApi=async(id)=>(await axiosInstance.get(`/api/products/${id}`)).data;
export const updateProductApi=async({id,payload})=>(await axiosInstance.patch(`/api/products/${id}`,payload)).data;
export const deleteMyProductApi=async(id)=>(await axiosInstance.delete(`/api/products/${id}`)).data;
export const getAdminProductsApi=async(params={})=>(await axiosInstance.get('/api/products/admin/all',{params})).data;
export const approveProductApi=async(id)=>(await axiosInstance.patch(`/api/products/${id}/approve`)).data;
export const rejectProductApi=async({id,rejectionReason})=>(await axiosInstance.patch(`/api/products/${id}/reject`,{rejectionReason})).data;
export const adminDeleteProductApi=async(id)=>(await axiosInstance.delete(`/api/products/${id}/admin`)).data;
